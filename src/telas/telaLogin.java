    public static Aluno autenticar(String matricula, String senha){
        AlunoDAO dao = new AlunoDAO();
        for(Aluno aluno: dao.read()){
            if(aluno.getSenha().equals(senha) && aluno.getMatricula().equals(matricula)){
                return aluno;
            }
        }  
        return null;
    }
    
